    public void refreshTable(){
        txtKodePelunasan.setText(null);
        txtJmlPelunasan.setText(null);
        txtStatusPelunasan.setText(null);

        //tabelPelunasan = new javax.swing.JTable();
        bindingGroup = new org.jdesktop.beansbinding.BindingGroup();
        PegadaianPUEntityManager = java.beans.Beans.isDesignTime() ? null : javax.persistence.Persistence.createEntityManagerFactory("PegadaianPU").createEntityManager();
        pelunasanQuery = java.beans.Beans.isDesignTime() ? null : PegadaianPUEntityManager.createQuery("SELECT p FROM Pelunasan p");
        pelunasanList = java.beans.Beans.isDesignTime() ? java.util.Collections.emptyList() : pelunasanQuery.getResultList();
        
        org.jdesktop.swingbinding.JTableBinding jTableBinding = org.jdesktop.swingbinding.SwingBindings.createJTableBinding(org.jdesktop.beansbinding.AutoBinding.UpdateStrategy.READ_WRITE, pelunasanList, tabelPelunasan);
        org.jdesktop.swingbinding.JTableBinding.ColumnBinding columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${kodePelunasan}"));
        columnBinding.setColumnName("Kode Pelunasan");
        columnBinding.setColumnClass(String.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${nasabah.namaNasabah}"));
        columnBinding.setColumnName("Nasabah");
        columnBinding.setColumnClass(String.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${jmlPelunasan}"));
        columnBinding.setColumnName("Jml Pelunasan");
        columnBinding.setColumnClass(Long.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${statusPelunasan}"));
        columnBinding.setColumnName("Status Pelunasan");
        columnBinding.setColumnClass(String.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${tglPelunasan}"));
        columnBinding.setColumnName("Tgl Pelunasan");
        columnBinding.setColumnClass(java.util.Date.class);
        bindingGroup.addBinding(jTableBinding);
        jTableBinding.bind();

        jScrollPane2.setViewportView(tabelPelunasan);
        bindingGroup.bind();
    }
    
    public void isicombo() {
        NasabahDAO ndao = new NasabahDAOimpl();
        ndao.getAll().stream().forEach((n) -> {
        comboNasabah.addItem(n.getNamaNasabah());
    });
    }

