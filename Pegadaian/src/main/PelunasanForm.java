    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        //tambah();
            Pelunasan p = new Pelunasan();
            PelunasanDAO pdao = new PelunasanDAOimpl();
            NasabahDAO ndao = new NasabahDAOimpl();
            p.setJmlPelunasan(Long.parseLong(txtJmlPelunasan.getText()));
            p.setKodePelunasan(txtKodePelunasan.getText());
            p.setStatusPelunasan(txtStatusPelunasan.getText());
            p.setTglPelunasan(Date.from(Instant.now()));
            p.setNasabah(ndao.getByNama(comboNasabah.getSelectedItem().toString()));
            pdao.insert(p);
            refreshTable();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        PelunasanDAO pdao = new PelunasanDAOimpl();
        Pelunasan p = pdao.getById(Long.parseLong(txtID.getText()));
        p.setKodePelunasan(txtKodePelunasan.getText());
        p.setJmlPelunasan(Long.parseLong(txtJmlPelunasan.getText()));
        p.setStatusPelunasan(txtStatusPelunasan.getText());
        pdao.update(p);
        refreshTable();
    }//GEN-LAST:event_btnUbahActionPerformed

